path = turtle.Turtle()
path.speed(0)
path.shape("circle")
path.turtlesize(0.3)
turtle.tracer()
path.penup()
path.setpos(0,300)

path.color("blue")

for pin in pins:
    path.setpos(pin[0], pin[1])
    path.pendown()
    path.circle(2)
    path.penup()
path.color("black")

path.setpos(0,300)
path.pendown()
turtle.colormode(255)

for i in range(balls):
    r = random.randint(0, 200)
    g = random.randint(0, 200)
    b = random.randint(0, 200)
    path.color(r, g, b)
    position = 0                        # start in top middle of grid
    path.setheading(270)
    for j in range(height):
        path.setheading(270)
        direc = random.randint(0, 1)
        if direc == 1:
            position += 1    # ball bounces off pin to the right
            path.fd(10)
            path.lt(90)
            path.fd(10)
        elif direc == 0:
            path.fd(10)
            path.rt(90)
            path.fd(10)
            position -= 1  # ball bounces off pin to the left
    result.append(position)
    path.setheading(270)
    path.fd(20)
    path.penup()
    path.goto(0, 300)
    path.pendown()

plt.hist(result, bins=range(1-height,height,2))
